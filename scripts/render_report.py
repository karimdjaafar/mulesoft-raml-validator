#!/usr/bin/env python3
"""
render_report.py — Render an audit report from findings.json.

Consumes the JSON output of audit_raml.py and renders it as either Markdown
or self-contained HTML. The HTML output uses neutral, accessible styling.

Usage:
    python scripts/render_report.py --findings findings.json --out report.md --format markdown
    python scripts/render_report.py --findings findings.json --out report.html --format html

Copyright (c) 2026 Karim DJAAFAR — Jasmine Conseil
Licensed under the Apache License, Version 2.0.
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import date
from pathlib import Path
from typing import Any

# ── Severity icons (unicode, no external assets) ────────────────────────────
SEVERITY_ICON = {
    "BLOCKER": "🛑",
    "MAJOR": "⚠️",
    "MINOR": "🔵",
    "INFO": "⚪",
}

VERDICT_ICON = {
    "GO": "✅",
    "GO-WITH-RESERVATIONS": "⚠️",
    "NO-GO": "🛑",
}


# ── Markdown rendering ──────────────────────────────────────────────────────


def render_markdown(payload: dict[str, Any]) -> str:
    findings = payload["findings"]
    counts = payload["counts"]
    score = payload["score"]
    verdict = payload["verdict"]
    raml_path = payload["raml_path"]

    lines: list[str] = []
    lines.append(f"# RAML Audit Report — `{raml_path}`")
    lines.append("")
    lines.append(f"**Audited file:** `{raml_path}`  ")
    lines.append(f"**Audit date:** {date.today().isoformat()}  ")
    lines.append("**Audit standard:** MuleSoft RAML 1.0 + API-Led Connectivity best practices  ")
    lines.append("**Auditor:** `mulesoft-raml-validator` (Community Edition)")
    lines.append("")
    lines.append("---")
    lines.append("")

    # Executive summary
    lines.append("## 1. Executive Summary")
    lines.append("")
    lines.append(f"| Metric | Value |")
    lines.append(f"|---|---|")
    lines.append(f"| **Readiness score** | **{score} / 100** |")
    lines.append(f"| **Verdict** | {VERDICT_ICON.get(verdict, '')} **{verdict}** |")
    lines.append(f"| BLOCKERs | {counts['BLOCKER']} |")
    lines.append(f"| MAJORs | {counts['MAJOR']} |")
    lines.append(f"| MINORs | {counts['MINOR']} |")
    lines.append(f"| INFOs | {counts['INFO']} |")
    lines.append(f"| Total findings | {payload['total_findings']} |")
    lines.append("")

    # Findings table
    if findings:
        lines.append("## 2. Findings Table")
        lines.append("")
        lines.append("| # | ID | Severity | Category | Location | Summary |")
        lines.append("|---|---|---|---|---|---|")
        for i, f in enumerate(findings, 1):
            icon = SEVERITY_ICON.get(f["severity"], "")
            lines.append(
                f"| {i} | `{f['rule_id']}` | {icon} {f['severity']} | "
                f"{f['category']} | {f['location']} | {f['summary']} |"
            )
        lines.append("")

        # Detailed findings
        lines.append("## 3. Detailed Findings")
        lines.append("")
        for f in findings:
            icon = SEVERITY_ICON.get(f["severity"], "")
            lines.append(f"### {icon} {f['severity']} — `{f['rule_id']}` · {f['summary']}")
            lines.append("")
            lines.append(f"**Location:** {f['location']}  ")
            lines.append(f"**Category:** {f['category']}")
            lines.append("")
            lines.append(f["detail"])
            lines.append("")
            lines.append("**Remediation:**")
            lines.append("")
            lines.append("```yaml")
            lines.append(f["remediation"])
            lines.append("```")
            lines.append("")
            lines.append("---")
            lines.append("")
    else:
        lines.append("## 2. Findings")
        lines.append("")
        lines.append("✅ No findings — the spec passes all 23 audit rules.")
        lines.append("")

    # Score appendix
    lines.append("## 4. Score Calculation")
    lines.append("")
    lines.append("Starting at 100, applying weights: -15/BLOCKER, -5/MAJOR, -2/MINOR, 0/INFO.")
    lines.append("")
    lines.append("| Severity | Count | Weight | Subtotal |")
    lines.append("|---|---|---|---|")
    lines.append(f"| BLOCKER | {counts['BLOCKER']} | -15 | {counts['BLOCKER'] * -15} |")
    lines.append(f"| MAJOR | {counts['MAJOR']} | -5 | {counts['MAJOR'] * -5} |")
    lines.append(f"| MINOR | {counts['MINOR']} | -2 | {counts['MINOR'] * -2} |")
    lines.append(f"| INFO | {counts['INFO']} | 0 | 0 |")
    lines.append("")
    lines.append(f"**Final score: {score} / 100 — Verdict: {verdict}.**")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append(
        "*Report generated by [`mulesoft-raml-validator`](https://github.com/karim-djaafar/mulesoft-raml-validator) "
        "(Community Edition, Apache 2.0).*"
    )

    return "\n".join(lines)


