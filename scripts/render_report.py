def html_escape(text: str) -> str:
    return (
        str(text)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def render_finding_html(f: dict[str, Any]) -> str:
    sev_class = f["severity"].lower()
    return f"""
<div class="finding finding-{sev_class}">
  <h3>
    <span class="badge badge-{sev_class}">{f['severity']}</span>
    <code>{html_escape(f['rule_id'])}</code> · {html_escape(f['summary'])}
  </h3>
  <p class="meta">
    <strong>Location:</strong> {html_escape(f['location'])} · 
    <strong>Category:</strong> {html_escape(f['category'])}
  </p>
  <p>{html_escape(f['detail'])}</p>
  <p><strong>Remediation:</strong></p>
  <pre><code>{html_escape(f['remediation'])}</code></pre>
</div>
"""


def render_html(payload: dict[str, Any]) -> str:
    counts = payload["counts"]
    verdict = payload["verdict"]
    verdict_class_map = {
        "GO": "go",
        "GO-WITH-RESERVATIONS": "reservations",
        "NO-GO": "nogo",
    }
    findings_html = (
        "\n".join(render_finding_html(f) for f in payload["findings"])
        if payload["findings"]
        else "<p>✅ No findings — the spec passes all 23 audit rules.</p>"
    )
    return HTML_TEMPLATE.format(
        raml_path=html_escape(payload["raml_path"]),
        date=date.today().isoformat(),
        score=payload["score"],
        verdict=verdict,
        verdict_class=verdict_class_map.get(verdict, "nogo"),
        verdict_icon=VERDICT_ICON.get(verdict, ""),
        blocker_count=counts["BLOCKER"],
        major_count=counts["MAJOR"],
        minor_count=counts["MINOR"],
        info_count=counts["INFO"],
        total_count=payload["total_findings"],
        findings_html=findings_html,
    )


# ── CLI ─────────────────────────────────────────────────────────────────────


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Render an audit report from findings.json."
    )
    parser.add_argument("--findings", type=Path, required=True, help="Path to findings.json.")
    parser.add_argument("--out", type=Path, required=True, help="Output file path.")
    parser.add_argument(
        "--format",
        choices=("markdown", "html"),
        default="markdown",
        help="Output format (default: markdown).",
    )
    args = parser.parse_args()

    try:
        payload = json.loads(args.findings.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError) as exc:
        sys.stderr.write(f"ERROR: cannot read findings: {exc}\n")
        return 1

    if args.format == "markdown":
        output = render_markdown(payload)
    else:
        output = render_html(payload)

    args.out.write_text(output, encoding="utf-8")
    sys.stdout.write(f"✓ Report rendered: {args.out} ({args.format})\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
